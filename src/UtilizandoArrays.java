public class UtilizandoArrays {

    public static void main(String[] args){

        String[] vet = new String[]{"Leonardo", "João", "Maria"};
        System.out.println("Os nomes informados foram: ");
        for(int cont = 0; cont < vet.length; cont++){
            System.out.println(vet[cont]);
        }
    }
}

